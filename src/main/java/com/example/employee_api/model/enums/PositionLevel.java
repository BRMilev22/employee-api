package com.example.employee_api.model.enums;

/**
 * Enum representing different position levels within the organization hierarchy
 */
public enum PositionLevel {
    ENTRY,       // Entry level positions
    JUNIOR,      // Junior level positions
    MID,         // Mid-level positions
    SENIOR,      // Senior level positions
    LEAD,        // Lead/Team Lead positions
    MANAGER,     // Management positions
    SENIOR_MANAGER,  // Senior management
    DIRECTOR,    // Director level
    VP,          // Vice President level
    EXECUTIVE    // Executive level (C-suite)
}